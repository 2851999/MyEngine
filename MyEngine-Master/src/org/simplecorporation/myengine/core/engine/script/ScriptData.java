package org.simplecorporation.myengine.core.engine.script;

public class ScriptData {
	
	/* Script data contains key words and basic syntax so that
	   the scripting language can be easily changed to make it
	   simpler and easier to use. */
	
	/*---------------The basic syntax---------------*/
	
	/* The import key word */
	public static final String SYNTAX_KEY_WORD_IMPORT = "uses";
	/* The call key word */
	public static final String SYNTAX_KEY_WORD_CALL = "call";
	/* The comment key word */
	public static final String SYNTAX_KEY_WORD_COMMENT = "#";
	/* The method key word */
	public static final String SYNTAX_KEY_WORD_METHOD = "method";
	/* The end method key word */
	public static final String SYNTAX_KEY_WORD_METHOD_END = "endmethod";
	/* The key word used after a method declaration */
	public static final String SYNTAX_KEY_WORD_METHOD_DECLARATION = ":";
	/* The key word separator */
	public static final String SYNTAX_KEY_WORD_SEPERATOR = " ";
	
}